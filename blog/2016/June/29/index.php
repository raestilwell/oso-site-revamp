				<h2>RiverPark Center offers Vivid Visual Art Camp</h2>
				<p><em>June 29, 2016</em></p>
				<p>Published 6/29/2016 Messenger-Inquirer</p>

				<p>By: Bobbie Hayes</p>

				<p>Rex Halterman sent his son to a Missoula Children’s Theatre camp as a young boy. The experience built a love for theater, and eventually, a career for his son. That’s one of the reasons he encourages sending children to arts programs, like the RiverPark Center’s Vivid Visual Art Camp that he’s teaching this week.</p>

				<p>Halterman has been a working artist for more than 40 years, and has been teaching for at least 10 of those years. His son is Eli Halterman, the RiverPark Center’s education director and coordinator of the camps.</p>

				<p>This week’s art camp has 21 students in attendance. Throughout the week, campers will create individual and collaborative art works and projects like sidewalk chalk art, and the mural they were working on Tuesday.</p>

				<p>The mural is composed of 90 different 8-by-10 painted panels each camper completed that — when all put together — made up a larger painting of a dinosaur. The multi-piece mural will be showcased in the RiverPark Center throughout the duration of the summer camps the center is offering this year.</p>

				<p>Dominic Ranallo, 11, of Owensboro, said when his father asked him if he would be interested in attending a summer camp geared toward the arts, he was excited. He said he likes art in school, and was looking forward to a whole week of doing art projects.</p>

				<p>“I’m learning new things about art and using materials I’ve never used before,” Dominic said, gesturing toward the custom apron he and his fellow campers made Monday on their first day of camp. “Yesterday we sewed together a puppet, which was really cool.”</p>

				<p>After campers worked on their paintings, they sat down with Carrie Olsen, from the Owensboro Symphony Orchestra to learn about music and art. They played music-themed bingo and Olsen taught them about Russian composer Modest Mussorgsky, who created a piece called “Pictures at an Exhibition.” The piece features music based on art works seen in an exhibition; in between each of the eight pieces, he included a promenade of his own feet walking from picture to picture.</p>

				<p>Ranallo was happy to learn about how music and art can be intertwined.</p>

				<p>“It’s cool to think about what color you think music is and that’s something I never thought about before,” he said. “You get to listen to music and think about art and it’s just awesome.”</p>

				<p>This arts camp will continue through the end of the week as part of the center’s four-part summer camp series. The remaining two camps are: Compass Creative Dramatics camp, which will focus on screenwriting, July 11-15; and Magnificent Magic camp, which will teach the secrets of various magic tricks, July 18-22.</p>

				<p>Registration is open for the last two camps and need-based scholarships are available on a first-come, first-served basis. Those interested in the scholarships can sign-up at the box office at 101 Daviess St.</p>

				<p>Camp prices are $200 per child for the first child and $175 per child for each additional child. To register, visit <a href="http://riverparkcenter.com" target="_blank">riverparkcenter.com</a> or the box office, or call 270-687-2770.</p>

