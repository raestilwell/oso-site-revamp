				<h2>Owensboro Symphony Music On Call Impacting Lives of Inmates</h2>
				<p><em>June 8, 2016</em></p>
				
				<p>OWENSBORO, KY – Over the last few weeks, The Owensboro Symphony Music On Call Community Engagement program has been making a powerful and positive impact on the lives of several inmates at the Daviess County Detention Center. Every Friday since the middle of May, women in the detention center who had an interest in being a part of a choir have been working together in preparation for a public performance at the Good Shepherd Church Celebration Sisterhood on Saturday, June 11, 2016 at 11:30 a.m.</p>

				<p>Music has the ability to make a tremendous impact on people’s lives, bringing new confidence, important transferable skills and raising their aspiration for the future. “One of the most rewarding experiences I have ever had is having this amazing opportunity to work with these beautiful ladies over the past few weeks,” said Cathy Mullins, Owensboro Symphony Music On Call Instructor. “The energy and excitement of these ladies and the special bond that we sharing through our music is very touching.”</p>

				<p>Jail and Prison Ministry Chaplain, Emil Herzog recently stated, “I heard a couple of songs after their first practice. The ladies sounded really good. There was a look of joy, satisfaction and even a little pride on their faces. There was a peace in that room.  I am planning to attend Saturday’s event to hear the ladies sing. I not only want to be an encouragement to them, I want to see and hear these ladies sing songs that have touched their hearts. I’m looking forward to seeing them touch the hearts of people who are in attendance.”</p>

				<p>Music On Call is designed to enhance and transform lives and bring comfort to those who are experiencing challenging circumstances. The program is made possible through the Owensboro Health Gives Community Benefit Grant.</p>

				<p>This Saturday’s “Celebration Sisterhood” event will be held at the Good Shepherd Church Recreation Center located at 3031 Bittel Road.  The event is open to the public with tickets costing $10 a piece. Tickets can be purchased online at gscowensboro.yapsody.com or by calling the church office at 270-684-8274.  Activities planned for this event will include inspirational speakers, brunch, fashion show,  live music, and a time of devotion.</p>
			
