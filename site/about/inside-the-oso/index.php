				<h2>A Commitment to the Community</h2>
				
				<p>The Owensboro Symphony Orchestra’s mission is to educate, entertain and enrich the lives of residents in Western Kentucky and Southern Indiana. The slogan of the Owensboro Symphony Academy remains “changing lives through the power of music.” We believe music changes lives. Looking ahead two decades, the OSO sees itself as a central player in the area’s ongoing efforts to create a more livable, prosperous and healthy community in which the differences that separate our residents increasingly diminish.</p>

				<p>The orchestra will do this through an ever-expanding program of music performance and music education that reaches into every segment of society. We will bring the healing and transforming power of music to venues of every kind, to residents of every age, class, race and place of residence. We will continue to educate, entertain and enrich the lives of residents, especially for the most vulnerable in our society — the young and disadvantaged.</p>

				<p>When people are asked which organizations bring important benefits to the community, the OSO will be among the first mentioned by a broad section of our residents.</p>

				<p>The Owensboro Symphony Orchestra (OSO) was founded in 1967. Since then, it has become a star among orchestras its size. For example, it remains one of the smallest orchestras nationwide to operate an academy, which opened nine years ago. Before the academy opened, the OSO offered about five music education programs and the Youth Orchestra included 35 students.</p>

				<p>Since opening the academy, the OSO’s music education offerings have grown to more than 20, serving about 20,000 students in 14 counties. And the Youth Orchestra’s ranks have tripled.</p>

				<p>The League of American Orchestras and Kennedy Center have highlighted some of the OSO’s education programs. Last year the OSO joined with the RiverPark Center and the Owensboro City and County Schools to participate in the Partners in Education at the “Kennedy Center”, Washington D.C. This program identifies the most effective arts educational programs.</p>
