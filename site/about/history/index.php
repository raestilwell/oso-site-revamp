				<h2>Years of Bringing Music to the Community</h2>
				
				<p>The Owensboro Symphony Orchestra (OSO) is the premier producer of live classical and pops music for the Western Kentucky region.  Under the leadership of Maestro Nick Palmer, Chief Executive Officer, G. Dan Griffith, and an active and dedicated Board of Directors, the OSO has increased programming nearly 50 percent in the last five years. The OSO performs more than 60 services annually, reaching an estimated audience of more than 50,000 per year.  Symphony performances take place at Cannon Hall in the RiverPark Center, which the city built in 1992 solely as the OSO’s concert hall.</p>

				<p>The symphony is a professional, per-service orchestra that contracts with about 120 musicians each season. The season includes Masterworks Series Classical Concerts, Pops Concerts, free summer concerts, Arts Teach Kids performances, and other concerts in Owensboro and throughout Kentucky and Southern Indiana.</p>

				<p>The OSO has been featured on In Performance at the Governor’s Mansion on Kentucky Educational Television and has recorded five CDs on commercial labels and the OSO’s own Symphony Discoveries label. The symphony frequently is featured on radio broadcasts.  It performed at Governor Paul Patton’s inauguration in December 1999 and at Centre College in Danville, Kentucky, prior to the 2000 Vice-Presidential Debate. Also, the symphony has toured with LeAnn Rimes, Anne Murray, Dionne Warwick and many other performers.</p>

				<p>An essential component of the symphony’s Community Outreach Program is its educational programming at the Owensboro Symphony Academy.  In addition to private and semi-private instruction, Kindermusik and Suzuki programs, the OSO offers more than 20 different educational programs to the community.  The OSO provides opportunities for growth and enrichment through its Youth and String Orchestras, Musicians in the Schools, a residency program by guest artists entitled “Guest on the Go,” a Young Artist Competition and School Day performances.  Thousands of students attend Masterworks series concerts each year under the auspices of Symphony Safari.</p>

				<p>The Owensboro Symphony Orchestra’s commitment to excellence is reflected by a high quality of performances and growth in programming.  The Owensboro Symphony Orchestra is committed to growth, to reaching a large and diverse audience, and to continuing to enrich the lives of Daviess County and regional residents well into the future.</p>

