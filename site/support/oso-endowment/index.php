				<h2>Foundation</h2>
				
				<p>The Endowment Fund of the Owensboro Symphony Orchestra is under the supervision and management of the Owensboro Symphony Orchestra Foundation Board of Governors. It is a plan to ensure the artistic future of the ensemble. Gifts and pledges will provide a stable financial base from which to plan for the future excellence of this fine orchestra.</p>

				<p>Many people who believe in the mission of the Owensboro Symphony have included charitable gifts in the final distribution of their estates. Making a gift from assets that remain when no longer needed can be done easily through a will bequest.</p>

				<h3>Exemplifying the Best</h3>

				<p>Owensboro Symphony Orchestra — a star among orchestras of its size and even larger — plays a powerful role in the cultural and economic life of Owensboro and Daviess County. Its performance level amazes first-time visitors, and its fiscal responsibility is equally outstanding in an era in which too many ensembles falter or cease operations. The Symphony, like Owensboro, seems to be a clear-cut American success story. But excellence has its price. Recognizing this economic reality, we have established an endowment fund to benefit The Owensboro Symphony. Endowment income will enable the Symphony to respond flexibly to the community’s needs, share its expertise for the benefit of others, and continue to provide the cultural enrichment that brightens thousands of lives each year.</p>

				<h3>Special Requests</h3>

				<p>One way to express your love to family, friends, and community is through a testamentary gift….a Will. Truly, this form of bequest is the ultimate gift. And, for The Owensboro Symphony, through the Foundation, it may be our best insurance for the future. It need not be an expensive, time-consuming process. If you have a Will, simply ask your attorney to amend it. By adding a paragraph or two in a codicil to your existing Will, you can reflect your desire to make a gift to The Owensboro Symphony Foundation.</p>

				<h3>Creative Gift Giving</h3>

				<p>A variety of charitable techniques are available for enthusiastic, creative giving. We urge you to consider:</p>

				<p><strong>A Gift of Appreciated Stock</strong></p>
				<ul>
					<li>Full Charitable deduction</li>
					<li>No capital gains taxes</li>
				</ul>
				
				<p><strong>A Gift of Life Insurance</strong></p>
				<ul>
					<li>Charitable tax deduction</li>
					<li>No estate taxes/probate costs</li>
					<li>A small premium for a substantial charitable gift</li>
				</ul>
				
				<p><strong>A Gift of Your Home, Farm or Other Real Estate While Retaining a Life Interest</strong></p>
				<ul>
					<li>Charitable tax deduction</li>
					<li>No estate taxes/probate costs</li>
					<li>Full use of the property for life</li>
				</ul>
				
				<p><strong>A Charitable Remainder Unitrust or Annuity Trust</strong></p>
				<ul>
					<li>No capital gains taxes</li>
					<li>Income for life</li>
					<li>Charitable tax deduction</li>
					<li>No estate taxes or probate costs</li>
					<li>Charities of your choice receive the remainder after your death</li>
				</ul>
					
				<p><strong>A Charitable Lead Trust or Lead Annuity Trust</strong></p>
				<ul>
					<li>Charitable tax deduction</li>
					<li>Charities receive funds for a term of years. Later, the remaining corpus flows to family members.</li>
				</ul>
				
				<h3>How Contributions Work</h3>

				<p>We put your gifts to work by investing in listed securities that produce income. Leaving the principal untouched, we use the income to fund current and future programs. The OSO Endowment Fund is under the supervision of the Symphony’s Foundation Board of Governors.</p>

				<h3>Priorities for New Endowment</h3>

				<p>Endowment income will support the strategies outlined in the Symphony’s Long-Range Plan, building on artistic quality, strengthening educational and outreach services, and securing fiscal stability for the Symphony.</p>
