				<h2>Kindermusik at The Owensboro Symphony Academy</h2>

				<p>The Owensboro Symphony Academy has had in-house Kindermusik classes since 2006 and outreach programs in Hager Elementary and H. L. Neblett Community Center since 2007.  All our Kindermusik outreach programs are made possible by grants and donations.  We are always looking for more ways to reach economically challenged youth, but are limited by the amount of support we receive.</p>

				<p>One of the primary goals of The Owensboro Symphony Academy is to reach at-risk children who could potentially start their school careers behind their peers academically.  Sometimes they never catch up. Kindermusik gives them an academic boost because it is a math- and reading-readiness program.  It also enhances communications skills and a child’s ability to self-regulate his or her behavior.  Giving children the skills to succeed academically creates greater self-esteem and less stress, both of which have multiple mental health and wellness benefits.</p>

 

				<h4>Kindermusik Overview & Benefits</h4>
				
				<p>All over the world parents bond with their babies through musical sounds and rhythmic movement.  They rock and swing their children, put them to sleep with lullabies and make them laugh with nursery rhymes.  Parents know instinctively what scientists have now proven: young children thrive with music. It’s one of the best vehicles for learning in early childhood development.  Kindermusik takes learning through music to the next level; its curriculum is proven to promote and improve child brain development. And because it is a class that involves the parents and children, it achieves a bonding between parent and child that few other activities could attain.</p>

				<p>Psychologists, neuroscientists, and experts in early childhood development have demonstrated that music does more for children than bring them joy; it helps their brain cells make the connections needed for virtually every kind of intelligence.  Kindermusik’s curriculum is built on this research.  When young children are consistently engaged by music in an age-appropriate, socially accepting environment, they benefit at many levels:</p>

				<ul>
					<li>Early Literacy – They gain the phonological processing, spoken language, and comprehension skills that are the foundation of reading.</li>
					<li>Quantitative – They build the spatial-temporal and reasoning skills required for math, science, and engineering.</li>
					<li>Social-Emotional – They develop social and emotional skills that are essential for school readiness—like the ability to regulate their responses and relate to others in complex ways.</li>
					<li>Physical – By moving and dancing to music and playing simple instruments, children improve their gross and fine motor skills.</li>
					<li>Creative – Activities that encourage freedom within a fun and friendly structure spark their creativity.</li>
					<li>Studies show that children involved with ongoing music programs don’t abuse alcohol and drugs at the same rate as students who never enroll in music programs.</li>
				</ul>

				<p>And of course, they develop a lifelong love of music!</p>
